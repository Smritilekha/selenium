package javaPgms;

public class SwapNumbers {

	public static void main(String args[])
	{
		int a=10,b=20,c;
		System.out.println("Before Swapping");
		System.out.println(a);
		System.out.println(b);
		c= a;
		a=b;
		b=c;
		System.out.println("After swapping");
		System.out.println(a);
		System.out.println(b);
		
	}
}
